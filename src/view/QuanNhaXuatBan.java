    private void tblnhaxuatbanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnhaxuatbanMouseClicked
        // TODO add your handling code here:
        int index = tblnhaxuatban.getSelectedRow();

        if (index != -1) {
            String maNXB = tblnhaxuatban.getValueAt(index, 0).toString();
            String tenNXB = tblnhaxuatban.getValueAt(index, 1).toString();
            String diaChi = tblnhaxuatban.getValueAt(index, 2).toString();
            String eMail = tblnhaxuatban.getValueAt(index, 3).toString();
           

            txtdiachi.setText(diaChi);
            txtmanxb.setText(maNXB);
            txttennxb.setText(tenNXB);
            txtemail.setText(eMail);
  txtmanxb.setEditable(false);
  txttennxb.setEditable(false);
           
        }
     
    }//GEN-LAST:event_tblnhaxuatbanMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (txtdiachi.getText().isEmpty() || txtemail.getText().isEmpty() || txtmanxb.getText().isEmpty() || txttennxb.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hết Dữ Liệu!");
            return;
        } else {
            String maNXB = txtmanxb.getText();
             String tenNXB = txttennxb.getText();
            for (NhaXuatBan nx : arr) {
                if (nx.getMaNXB().equals(maNXB)) {
                    JOptionPane.showMessageDialog(this, "Mã Này Đã Tồn Tại!");
                    return;
                }
                if (nx.getTenNXB().equals(tenNXB)) {
                    JOptionPane.showMessageDialog(this, "Tên Đã Tồn Tại!");
                    return;
                }
            }
        }
        NhaXuatBan nx = new NhaXuatBan();
        nx.setMaNXB(txtmanxb.getText());
        nx.setTenNXB(txttennxb.getText());
        nx.setDiaChi(txtdiachi.getText());
        nx.setEmail(txtemail.getText());

        arr.add(nx);
        servicer.addNhaXuatBan(nx);
        viewdata(arr);
        JOptionPane.showMessageDialog(this, "Thêm Thành Công!");
    }//GEN-LAST:event_btnthemActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int index = tblnhaxuatban.getSelectedRow();
        if (index == - 1) {
            JOptionPane.showMessageDialog(this, "Chưa Chọn Dòng Để Cập Nhật!");
        } else {
            
            NhaXuatBan nx = arr.get(index);
            nx.setMaNXB(txtmanxb.getText());
            nx.setTenNXB(txttennxb.getText());
            nx.setDiaChi(txtdiachi.getText());
            nx.setEmail(txtemail.getText());
          

            servicer.updateNhaXuatBan(nx);
            viewdata(arr);
            JOptionPane.showMessageDialog(this, "Update Thành Công!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

