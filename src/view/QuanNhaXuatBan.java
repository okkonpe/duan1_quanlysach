    public void viewdata(ArrayList<NhaXuatBan> searchResults) {
        if (!searchResults.isEmpty()) {
            // Clear the table before displaying new data
            model.setRowCount(0);

            // Loop over searchResults and add each result to the table
            for (NhaXuatBan nx : searchResults) {
                Object[] row = new Object[]{nx.getMaNXB(), nx.getTenNXB(), nx.getDiaChi(), nx.getEmail()};
                model.addRow(row);
            }
        } else {
            // Optional: if no results, clear the table to show empty state
            model.setRowCount(0);
        }
    }

