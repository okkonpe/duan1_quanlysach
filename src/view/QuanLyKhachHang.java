    public void viewdata( List<KhachHang> khachHangList) {
        model.setRowCount(0);

        for (KhachHang kh : khachHangList) {
            Object[] row = new Object[]{kh.getMaKhachHang(), kh.getTenKh(), kh.getNgaySinh(), kh.getsĐt(), kh.geteMail(), kh.isGioiTinh() ? "Nam" : "Nữ", kh.getDiaChi()};
            model.addRow(row);
        }
    }

    public void ititdata(int index) {
        KhachHang kh = arr.get(index);
        txtmakh.setText(kh.getMaKhachHang());
        txthoten.setText(kh.getTenKh());
        txtdiachi.setText(kh.getDiaChi());
        txtemail.setText(kh.geteMail());
        txtsdt.setText(kh.getsĐt());
        if (kh.isGioiTinh()) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        datangaysinh.setDate(kh.getNgaySinh());
    }

