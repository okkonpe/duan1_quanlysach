    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (txtmakh.getText().isEmpty() || txthoten.getText().isEmpty() || txtdiachi.getText().isEmpty() || txtsdt.getText().isEmpty() || txtemail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Dữ Liệu!");
            return;
        }
        String maKh = txtmakh.getText();
        for (KhachHang nv : arr) {
            if (nv.getMaKhachHang().equals(maKh)) {
                JOptionPane.showMessageDialog(this, "Mã Này Đã Tồn Tại!");
                return;
            }
        }
        KhachHang kh = new KhachHang();
        kh.setMaKhachHang(txtmakh.getText());
        kh.setTenKh(txthoten.getText());
        kh.setDiaChi(txtdiachi.getText());
        kh.seteMail(txtemail.getText());
        kh.setsĐt(txtsdt.getText());
        if (rdonam.isSelected()) {
            kh.setGioiTinh(true);
        } else if (rdonu.isSelected()) {
            kh.setGioiTinh(false);
        }
        kh.setNgaySinh(datangaysinh.getDate());
        java.util.Date utilDate = datangaysinh.getDate();

        if (utilDate != null) {
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            kh.setNgaySinh(sqlDate);
        }
        arr.add(kh);
        serviec.addKhachHang(kh);
        viewdata(arr);
        JOptionPane.showMessageDialog(this, "Thêm Thành Công!");
    }//GEN-LAST:event_btnthemActionPerformed

