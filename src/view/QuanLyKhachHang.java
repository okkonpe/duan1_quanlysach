    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Lưu file Excel");
        fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Excel Files", "xlsx"));

        // Hiện hộp thoại lưu file
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();

            // Kiểm tra xem file có đuôi .xlsx không
            if (!fileToSave.getAbsolutePath().endsWith(".xlsx")) {
                fileToSave = new File(fileToSave.getAbsolutePath() + ".xlsx");
            }

            try (Workbook workbook = new XSSFWorkbook()) {
                Sheet sheet = workbook.createSheet("Khách Hàng");
                Row headerRow = sheet.createRow(0);

                // Tạo tiêu đề cột
                headerRow.createCell(0).setCellValue("Mã Khách Hàng");
                headerRow.createCell(1).setCellValue("Họ Tên");
                headerRow.createCell(2).setCellValue("Ngày Sinh");
                headerRow.createCell(3).setCellValue("Số Điện Thoại");
                headerRow.createCell(4).setCellValue("Email");
                headerRow.createCell(5).setCellValue("Giới Tính");
                headerRow.createCell(6).setCellValue("Địa Chỉ");

                // Xuất dữ liệu từ JTable ra file Excel
                for (int i = 0; i < model.getRowCount(); i++) {
                    Row row = sheet.createRow(i + 1);
                    for (int j = 0; j < model.getColumnCount(); j++) {
                        Object cellValue = model.getValueAt(i, j);
                        row.createCell(j).setCellValue(cellValue != null ? cellValue.toString() : "");
                    }
                }

                // Lưu file Excel
                try (FileOutputStream outputStream = new FileOutputStream(fileToSave)) {
                    workbook.write(outputStream);
                    JOptionPane.showMessageDialog(this, "Xuất Excel thành công!");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi khi xuất Excel: " + e.getMessage());
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

