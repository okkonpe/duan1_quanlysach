    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int index = tbltheloai.getSelectedRow();

        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Chưa Chọn Dòng Để Cập Nhật!");
        } 
          String tenTL = txttentheloai.getText();
            for (TheLoai tl : arr) {              
                 if (tl.getTenTheLoai().equals(tenTL)) {
                    JOptionPane.showMessageDialog(this, "Tên Thể Loại Đã Tồn Tại!");
                    return;
                }
            }
        TheLoai tl = arr.get(index);
        tl.setMaTheLoai(txtmatheloai.getText());
        tl.setTenTheLoai(txttentheloai.getText());
       
        sercecer.UpdateTheLoai(tl);
        viewdata();
        JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công!");
    }//GEN-LAST:event_jButton2ActionPerformed


