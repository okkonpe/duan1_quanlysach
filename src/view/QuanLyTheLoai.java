    private void tbltheloaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltheloaiMouseClicked
        // TODO add your handling code here:
        int row = tbltheloai.getSelectedRow();

        if (row != -1) { // Kiểm tra nếu có dòng được chọn
            // Lấy thông tin mã thể loại và tên thể loại từ bảng
            String maTheLoai = tbltheloai.getValueAt(row, 0).toString(); // Cột 0 là mã thể loại
            String tenTheLoai = tbltheloai.getValueAt(row, 1).toString(); // Cột 1 là tên thể loại
          
            // Hiển thị thông tin lên các ô nhập liệu
            txtmatheloai.setText(maTheLoai);
            txttentheloai.setText(tenTheLoai);
          

            // Ngừng cho phép chỉnh sửa mã thể loại
         
        }
    }//GEN-LAST:event_tbltheloaiMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (txtmatheloai.getText().isEmpty() || txttentheloai.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Hết Dữ Liệu!");
            return;
        } else {
            String maTL = txtmatheloai.getText();
             String tenTL = txttentheloai.getText();
            for (TheLoai tl : arr) {
                if (tl.getMaTheLoai().equals(maTL)) {
                    JOptionPane.showMessageDialog(this, "Mã Thể Loại Trùng!");
                    return;
                }
                 if (tl.getTenTheLoai().equals(tenTL)) {
                    JOptionPane.showMessageDialog(this, "Tên Thể Loại Đã Tồn Tại!");
                    return;
                }
            }
        }
        TheLoai tl = new TheLoai();
        tl.setMaTheLoai(txtmatheloai.getText());
        tl.setTenTheLoai(txttentheloai.getText());     
        arr.add(tl);
        sercecer.addTheLoai(tl);
        viewdata();
        JOptionPane.showMessageDialog(this, "Bạn Thêm Thành Công!");
    }//GEN-LAST:event_jButton1ActionPerformed

