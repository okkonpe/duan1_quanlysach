    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (txtmanhanvien.getText().isEmpty() || txttennhanvien.getText().isEmpty()
                || txtdiachi.getText().isEmpty() || txtsodienthoai.getText().isEmpty()
                || txttaikhoan.getText().isEmpty() || txtmatkhau.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn Chưa Nhập Dữ Liệu!");
            return;
        }

        String maNV = txtmanhanvien.getText();
        for (NhanVien nv : arr) {
            if (nv.getMaNhanVien().equals(maNV)) {
                JOptionPane.showMessageDialog(this, "Mã Này Đã Tồn Tại!");
                return;
            }
        }

        NhanVien nv = new NhanVien();
        nv.setMaNhanVien(maNV);
        nv.setTen(txttennhanvien.getText());
        nv.setGioiTinh(rdonam.isSelected());
        nv.setDiaChi(txtdiachi.getText());
        nv.setSđt(txtsodienthoai.getText());
        nv.setTenTaiKhoan(txttaikhoan.getText());
        nv.setMatKhau(txtmatkhau.getText());
        nv.setQuyen((String) cboquyen.getSelectedItem());
        nv.setTrangThai(rdodanglam.isSelected());

        arr.add(nv);
        sevicer.addNhanVien(nv);
        viewdata(arr);
        JOptionPane.showMessageDialog(this, "Thêm Thành Công!");
    }//GEN-LAST:event_btnthemActionPerformed

