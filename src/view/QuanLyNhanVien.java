    public void viewdata(List<NhanVien> nhanVienList) {
        model.setRowCount(0);
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        for (NhanVien nv : nhanVienList) {
            Object[] row = new Object[]{
                nv.getMaNhanVien(), nv.getTenTaiKhoan(), nv.getMatKhau(),
                nv.getQuyen(), nv.getTen(), nv.isGioiTinh() ? "Nam" : "Nữ",
                nv.getDiaChi(), nv.getSđt(),
                nv.isTrangThai() ? "Đang Làm" : "Nghỉ Làm"
            };
            model.addRow(row);
        }
    }

